import java.util.*; 
	public class Odd{ 
    public static void main(String args[]){ 
        System.out.println("even numbers are:"); 
        for(int i=1;i<=100;i++){ 
            if(i%2==0) 
              System.out.print(i+"\n "); 
        } 
 
        System.out.println("\nodd numbers are:");         
		for(int i=1;i<=100;i++){ 
            if(i%2!=0) 
              System.out.print(i+" "); 
        } 
    } 
  } 
